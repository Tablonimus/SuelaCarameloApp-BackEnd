import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  email: {
    type: String,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["admin", "reporter", "reviewer"],
    default: "reporter",
  },
}, {
  timestamps: true,
});

export default mongoose.model("User", userSchema);
